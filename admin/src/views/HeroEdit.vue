<script>
export default {
  props: {
    id: {},
  },
  data() {
    return {
      categories: [],
      items: [],
      model: {
        name: "",
        avatar: "",
        scores: {
          difficult: 0,
        },
        skills: [],
      },
      value: "",
      parents: [],
    };
  },
  created() {
    // console.log(this.$route.path);
    this.id && this.fetch();
    this.fetchParents();
    this.fetchCategories();
    this.fetchItems();
  },

  watch: {
    //   监听路由变化，当进入分类编辑页面的时候，输入框有对应填好的分类名称，这时候如果点击新建分类，就要是输入框里面的分类名称消失（这里用watch监听路由变化）
    $route: {
      handler(val, oldVal) {
        if (val.fullPath === "/heroes/create") {
          this.model.name = this.model.parent = "";
          //在这里做你要做的事
        }
        console.log(oldVal);
      },
      deep: true, //深度监听
    },
  },
  methods: {
    afterUpload(res) {
      this.$set(this.model, "avatar", res.url);
      // this.model.icon = res.url;
    },
    async fetch() {
      const res = await this.$http.get(`rest/heroes/${this.id}`);
      // this.model = res.data;
      this.model = Object.assign({}, this.model, res.data);
      console.log("123", this.model);
    },
    async fetchCategories() {
      const res = await this.$http.get(`rest/categories`);
      this.categories = res.data;
    },
    async fetchItems() {
      const res = await this.$http.get(`rest/items`);
      this.items = res.data;
    },
    async fetchParents() {
      const parentsCates = await this.$http.get(`rest/heroes`);
      console.log(parentsCates.data);
      this.parents = parentsCates.data;
    },
    async save() {
      // this.$http()
      // console.log(123);
      let res; // eslint-disable-line no-unused-vars
      if (this.id) {
        res = await this.$http.put(`rest/heroes/${this.id}`, this.model); // eslint-disable-line no-unused-vars
      } else {
        res = await this.$http.post("rest/heroes", this.model); // eslint-disable-line no-unused-vars
      }
      this.$router.push("/heroes/list");
      this.$message({
        type: true,
        message: "保存成功",
      });
    },
  },
};

</script>
